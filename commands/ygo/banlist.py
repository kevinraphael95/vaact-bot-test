import discord
from discord.ext import commands
import aiohttp
from bs4 import BeautifulSoup

class Banlist(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    @commands.command(name="banlist", aliases=["bl"])
    async def banlist(self, ctx, statut: str = "ban"):
        """
        Affiche les cartes bannies, limit√©es ou semi-limit√©es en TCG.
        Utilisation : !banlist ban / limit√© / semi-limit√© ou b / l / sl
        """

        # Mapping des statuts possibles
        mapping = {
            "ban": "Interdites",
            "b": "Interdites",
            "limit√©": "Limit√©es",
            "l": "Limit√©es",
            "semi-limit√©": "Semi-Limit√©es",
            "sl": "Semi-Limit√©es"
        }

        statut = statut.lower()
        if statut not in mapping:
            await ctx.send("‚ùå Statut invalide. Utilisez `ban`, `limit√©`, `semi-limit√©`, ou leurs raccourcis (`b`, `l`, `sl`).")
            return

        statut_fr = mapping[statut]
        url = "https://www.db.yugioh-card.com/yugiohdb/forbidden_limited.action"

        await ctx.send(f"üîÑ R√©cup√©ration des cartes **{statut_fr}** depuis le site officiel...")

        async with aiohttp.ClientSession() as session:
            async with session.get(url) as resp:
                if resp.status != 200:
                    await ctx.send("‚ùå Impossible de r√©cup√©rer les donn√©es depuis le site officiel.")
                    return
                html = await resp.text()

        soup = BeautifulSoup(html, 'html.parser')

        # Extraction des cartes selon le statut
        sections = soup.find_all("section", class_="forbidden")
        cartes = []

        for section in sections:
            header = section.find("h3")
            if header and statut_fr.lower() in header.text.lower():
                card_elements = section.find_all("span", class_="card_name")
                cartes = [card.text.strip() for card in card_elements]
                break

        if not cartes:
            await ctx.send(f"‚ùå Aucune carte trouv√©e avec le statut **{statut_fr}**.")
            return

        # Envoi des cartes par blocs (max 30 par embed)
        chunk_size = 30
        for i in range(0, len(cartes), chunk_size):
            chunk = cartes[i:i+chunk_size]
            embed = discord.Embed(
                title=f"üìã Cartes {statut_fr} (TCG)",
                description="\n".join(chunk),
                color=discord.Color.red() if statut_fr == "Interdites" else (
                    discord.Color.orange() if statut_fr == "Limit√©es" else discord.Color.gold()
                )
            )
            await ctx.send(embed=embed)

async def setup(bot):
    await bot.add_cog(Banlist(bot))
