    async def testquestion(self, ctx):
        try:
            # üîÑ Chargement d‚Äôun √©chantillon de cartes
            cards = await self.fetch_card_sample()
            if not cards:
                await ctx.send("üö® Erreur lors du chargement des cartes.")
                return

            # üß† S√©lection intelligente : carte avec arch√©type + au moins 4 dans le groupe
            group = None
            for _ in range(10):  # Tentatives limit√©es pour performance
                candidate = random.choice(cards)
                archetype = candidate.get("archetype")
                if not archetype:
                    continue
                same_type_cards = [
                    c for c in cards if c.get("archetype") == archetype and "desc" in c and "name" in c
                ]
                if len(same_type_cards) >= 4:
                    group = same_type_cards
                    break

            if not group:
                await ctx.send("‚ùå Pas assez de cartes du m√™me arch√©type. R√©essaie plus tard.")
                return

            # üéØ Choix de la bonne r√©ponse et des fausses
            true_card = random.choice(group)
            wrong_choices = random.sample(
                [c for c in group if c["name"] != true_card["name"]], 3
            )

            all_choices = [true_card["name"]] + [c["name"] for c in wrong_choices]
            random.shuffle(all_choices)
            correct_index = all_choices.index(true_card["name"])

            # üìÑ Description avec nom masqu√©
            desc = self.censor_card_name(true_card["desc"], true_card["name"])

            # üé® Construction de l'embed
            embed = discord.Embed(
                title=f"üß© Arch√©type : {true_card.get('archetype', 'Inconnu')}",
                description=desc[:300] + ("..." if len(desc) > 300 else ""),
                color=discord.Color.teal()
            )
            embed.add_field(name="Type", value=true_card.get("type", "‚Äî"), inline=True)

            if true_card.get("type", "").lower().startswith("monstre"):
                embed.add_field(name="ATK", value=str(true_card.get("atk", "‚Äî")), inline=True)
                embed.add_field(name="DEF", value=str(true_card.get("def", "‚Äî")), inline=True)
                embed.add_field(name="Niveau", value=str(true_card.get("level", "‚Äî")), inline=True)
                embed.add_field(name="Attribut", value=true_card.get("attribute", "‚Äî"), inline=True)

            options = "\n".join([f"{REACTIONS[i]} {name}" for i, name in enumerate(all_choices)])
            embed.add_field(name="Quel est le nom de cette carte ?", value=options, inline=False)

            msg = await ctx.send(embed=embed)

            # üó≥Ô∏è Ajout des r√©actions de vote
            for emoji in REACTIONS[:len(all_choices)]:
                await msg.add_reaction(emoji)

            def check(reaction, user):
                return user == ctx.author and reaction.message.id == msg.id and str(reaction.emoji) in REACTIONS

            try:
                reaction, _ = await self.bot.wait_for("reaction_add", timeout=600.0, check=check)
            except asyncio.TimeoutError:
                await ctx.send("‚è∞ Temps √©coul√© !")
                return

            selected_index = REACTIONS.index(str(reaction.emoji))
            user_id = str(ctx.author.id)

            if selected_index == correct_index:
                await self.update_streak(user_id, correct=True)
                await ctx.send(f"‚úÖ Bonne r√©ponse ! C‚Äô√©tait bien **{true_card['name']}**.")
            else:
                await self.update_streak(user_id, correct=False)
                await ctx.send(f"‚ùå Mauvaise r√©ponse ! C‚Äô√©tait **{true_card['name']}**. S√©rie r√©initialis√©e.")

        except Exception as e:
            print("[ERREUR TESTQUESTION]", e)
            await ctx.send("üö® Une erreur est survenue.")

