class Deck(commands.Cog):
    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="deck",
        help="Affiche les decks du tournoi VAACT, organisÃ©s par saison.",
        description="Affiche une interface interactive pour choisir une saison et un duelliste."
    )
    async def deck(self, ctx: commands.Context):
        try:
            deck_data = load_deck_data()
            view = DeckSelectView(self.bot, deck_data)
            await ctx.send("ğŸ“¦ Choisis une saison :", view=view)
        except Exception as e:
            print("[ERREUR DECK]", e)
            await ctx.send("âŒ Une erreur est survenue lors du chargement des decks.")

